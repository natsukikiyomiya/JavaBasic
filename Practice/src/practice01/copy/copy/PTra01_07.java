public class PTra01_07 {
	public static void main(String[] args) {

		// 基本型8種類の変数を宣言します
		boolean isEat;
		byte myAge;
		short text;
		char gender;
		int age;
		long matle;
		float head;
		double crow;
		// ★ それぞれの型変数に、値を代入してください
		// ※ 値は、好きな数字を入力してください
        isEat = false;
        myAge = 20;
        text = 47;
        gender = '男';
        age = 100;
        matle = 23000;
        head = 1;
        crow = 1.2;
		// ★ それぞれの変数の中身を出力してください
        System.out.println(isEat);
        System.out.println(myAge);
        System.out.println(text);
        System.out.println(gender);
        System.out.println(age);
        System.out.println(matle);
        System.out.println(head);
        System.out.println(crow);
	}
}
